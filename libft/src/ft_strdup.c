#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	int		i;

	if (!s1)
		return (NULL);
	if (!(s = ft_strnew(ft_strlen(s1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = s1[i];
	return (s);
}
