#include "libft.h"

void		ft_setdoubletozero(double *a, double *b, double *c, double *d)
{
	if (a)
		*a = 0.0;
	if (b)
		*b = 0.0;
	if (c)
		*c = 0.0;
	if (d)
		*d = 0.0;
	return ;
}
