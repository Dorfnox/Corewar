#include "libft.h"

int		ft_striswhitespace(char *s)
{
	int		i;

	if (!s)
		return (0);
	i = -1;
	while (s[++i])
		if (!(s[i] == ' ' || s[i] == '\t' || s[i] == '\n' ||
				s[i] == '\v' || s[i] == '\f' || s[i] == '\r'))
			return (0);
	return (1);
}
