#include "corewar.h"

void	init_c_array(t_corewar *c)
{
	uint16_t		i;
	uint16_t		j;
	char			*t;

	t = "0123456789ABCDEF";
	i = -1;
	while (++i < 256)
	{
		c->c_array[i][0] = t[i / 16];
		c->c_array[i][1] = t[i % 16];
	}
}